#![cfg(feature = "segmentation-dict-my")]
//! Dictionary-based Burmese word segmentation (feature `segmentation-dict-my`).
//!
//! Expected segmentations were produced by V8's
//! `Intl.Segmenter('my', {granularity:'word'})`, which uses the same ICU
//! `burmesedict` + `BurmeseBreakEngine` this module reuses (the Thai engine with
//! Burmese character classes and no suffix handling). Every case here matches V8
//! exactly, tokens and all.

use intl::unicode::words;

fn w(s: &str) -> Vec<&str> {
    words(s).collect()
}

#[test]
fn my_sentences_match_v8() {
    let cases: &[(&str, &[&str])] = &[
        (
            "ကျွန်တော်မြန်မာစာကိုချစ်တယ်",
            &["ကျွန်တော်", "မြန်မာ", "စာ", "ကို", "ချစ်", "တယ်"],
        ),
        ("မင်္ဂလာပါကမ္ဘာလောက", &["မင်္ဂလာ", "ပါ", "ကမ္ဘာ", "လောက"]),
        (
            "မြန်မာနိုင်ငံမှာလူဦးရေများတယ်",
            &["မြန်မာနိုင်ငံ", "မှာ", "လူဦးရေ", "များ", "တယ်"],
        ),
        (
            "ကျွန်တော်ကျောင်းကိုနေ့တိုင်းသွားတယ်",
            &["ကျွန်တော်", "ကျောင်း", "ကို", "နေ့", "တိုင်း", "သွား", "တယ်"],
        ),
        ("ဒီနေ့ရာသီဥတုကောင်းတယ်", &["ဒီ", "နေ့", "ရာသီဥတု", "ကောင်း", "တယ်"]),
        (
            "မြန်မာစာဟာရုံးသုံးဘာသာစကားဖြစ်တယ်",
            &["မြန်မာ", "စာ", "ဟာ", "ရုံးသုံးဘာသာ", "စကား", "ဖြစ်", "တယ်"],
        ),
        (
            "ကျွန်မမြန်မာအစားအစာကိုကြိုက်တယ်",
            &["ကျွန်မ", "မြန်မာ", "အစားအစာ", "ကို", "ကြိုက်", "တယ်"],
        ),
        (
            "ကျွန်တော်တို့အတူတကွသင်ယူကြတယ်",
            &["ကျွန်တော်", "တို့", "အတူတကွ", "သင်ယူ", "ကြ", "တယ်"],
        ),
        ("မင်းနေကောင်းလား", &["မင်း", "နေ", "ကောင်း", "လား"]),
        (
            "ကလေးတွေဥယျာဉ်ထဲမှာကစားနေတယ်",
            &["ကလေး", "တွေ", "ဥယျာဉ်", "ထဲ", "မှာ", "ကစား", "နေ", "တယ်"],
        ),
        (
            "ကျွန်တော်နိုင်ငံခြားဘာသာစကားသင်ချင်တယ်",
            &["ကျွန်တော်", "နိုင်ငံခြား", "ဘာသာစကား", "သင်", "ချင်", "တယ်"],
        ),
        (
            "သူတို့စာအုပ်ဖတ်နေကြတယ်",
            &["သူ", "တို့", "စာအုပ်", "ဖတ်", "နေ", "ကြ", "တယ်"],
        ),
        (
            "မင်္ဂလာပါ, ကမ္ဘာလောက!",
            &["မင်္ဂလာ", "ပါ", ",", " ", "ကမ္ဘာ", "လောက", "!"],
        ),
        (
            "ကျွန်တော် ၂၀၂၄ ခုနှစ်မှာမွေးတယ်",
            &["ကျွန်တော်", " ", "၂၀၂၄", " ", "ခု", "နှစ်", "မှာ", "မွေး", "တယ်"],
        ),
        ("သင်နေကောင်းလား?", &["သင်", "နေ", "ကောင်း", "လား", "?"]),
        (
            "ကျွန်တော်ဈေးကိုသွားပြီးအသီးအနှံဝယ်မယ်",
            &["ကျွန်တော်", "ဈေး", "ကို", "သွား", "ပြီး", "အသီးအနှံ", "ဝယ်", "မယ်"],
        ),
        (
            "မြန်မာနိုင်ငံမှာသမိုင်းရှည်လျားတယ်",
            &["မြန်မာနိုင်ငံ", "မှာ", "သမိုင်း", "ရှည်လျား", "တယ်"],
        ),
        (
            "ရန်ကုန်မြို့ဟာမြန်မာနိုင်ငံရဲ့မြို့တော်ဟောင်းဖြစ်တယ်",
            &[
                "ရန်",
                "ကုန်",
                "မြို့",
                "ဟာ",
                "မြန်မာနိုင်ငံ",
                "ရဲ့",
                "မြို့တော်",
                "ဟောင်း",
                "ဖြစ်",
                "တယ်",
            ],
        ),
        (
            "ကျွန်တော်မင်းကိုအရမ်းချစ်တယ်",
            &["ကျွန်တော်", "မင်း", "ကို", "အရမ်း", "ချစ်", "တယ်"],
        ),
        (
            "မြန်မာအစားအစာအရမ်းအရသာရှိတယ်",
            &["မြန်မာ", "အစားအစာ", "အရမ်း", "အရသာ", "ရှိ", "တယ်"],
        ),
        ("တနင်္လာ အင်္ဂါ ဗုဒ္ဓဟူး", &["တနင်္လာ", " ", "အင်္ဂါ", " ", "ဗုဒ္ဓဟူး"]),
        (
            "ကျောင်းသားတွေတက္ကသိုလ်မှာပညာသင်ကြားနေတယ်",
            &[
                "ကျောင်းသား",
                "တွေ",
                "တက္ကသိုလ်",
                "မှာ",
                "ပညာသင်",
                "ကြား",
                "နေ",
                "တယ်",
            ],
        ),
        (
            "ဒီအလုပ်ကကျွန်တော့်အတွက်ခက်ခဲတယ်",
            &["ဒီ", "အလုပ်", "က", "ကျွန်", "တော့်", "အတွက်", "ခက်ခဲ", "တယ်"],
        ),
        ("မနက်ဖြန်မှာသင့်ကိုတွေ့မယ်", &["မနက်ဖြန်", "မှာ", "သင့်", "ကို", "တွေ့", "မယ်"]),
        (
            "ဧရာဝတီမြစ်ဟာမြန်မာနိုင်ငံကိုဖြတ်စီးတယ်",
            &["ဧရာဝတီ", "မြစ်", "ဟာ", "မြန်မာနိုင်ငံ", "ကို", "ဖြတ်", "စီး", "တယ်"],
        ),
        (
            "နေပြည်တော်ဟာမြန်မာနိုင်ငံရဲ့မြို့တော်ဖြစ်တယ်",
            &["နေပြည်တော်", "ဟာ", "မြန်မာနိုင်ငံ", "ရဲ့", "မြို့တော်", "ဖြစ်", "တယ်"],
        ),
        (
            "အကူအညီအတွက်ကျေးဇူးအများကြီးတင်ပါတယ်",
            &["အကူအညီ", "အတွက်", "ကျေးဇူး", "အများ", "ကြီး", "တင်", "ပါ", "တယ်"],
        ),
    ];
    for (input, expected) in cases {
        assert_eq!(&w(input), expected, "input: {input}");
    }
}

#[test]
fn burmese_mixed_with_punctuation() {
    // Surrounding punctuation/space keeps its UAX #29 behavior; the Burmese run is
    // subdivided by the dictionary. (Matches V8.)
    assert_eq!(
        w("မင်္ဂလာပါ, ကမ္ဘာလောက!"),
        ["မင်္ဂလာ", "ပါ", ",", " ", "ကမ္ဘာ", "လောက", "!"]
    );
}
